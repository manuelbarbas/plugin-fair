import { Address, Hash } from 'viem';

export type BiteConfig = 'manual' | 'automatic';

export interface PluginConfig {
  /**
   * Mapping of chain name to the chain details
   */
  chains: Record<string, ChainConfig>;
}

export interface ChainConfig {
  chainId: string;
  nativeToken: string;
  rpc: string;
  explorerUrl: string;
  /**
   * Mapping of token name to the token address
   */
  tokens: Record<string, string>;
  uniswapV2Router: string;
  biteDefault: boolean;
}

// Action parameters
export interface GetBalanceParams {
  chainName: string;
  address?: Address;
  token: string;
}

export interface EVMTransaction {
  to: string;
  data: `0x${string}`;
  value?: bigint;
  gas?: bigint;
  gasPrice?: bigint;
  chainId: string;
  nonce?: number;
}

export interface Transaction {
  to: string;
  data: string;
}

export interface TransferParams {
  chainName: string;
  token?: string;
  amount?: string;
  toAddress: Address;
  data?: `0x${string}`;
  isBite?: boolean;
}

export interface SwapParams {
  chainName: string;
  inputToken: string;
  outputToken: string;
  amount: string;
  slippage?: number;
  isBite?: boolean;
}

// Action return types
export interface GetBalanceResponse {
  chainName: string;
  address: Address;
  balance?: { token: string; amount: string };
}

export interface TransferResponse {
  chainName: string;
  txHash: Hash;
  recipient: Address;
  amount: string;
  token: string;
  data?: `0x${string}`;
  isBite?: boolean;
}

export interface SwapResponse {
  chainName: string;
  txHash: Hash;
  inputToken: string;
  outputToken: string;
  amountIn: string;
  amountOut: string;
  isBite?: boolean;
}

